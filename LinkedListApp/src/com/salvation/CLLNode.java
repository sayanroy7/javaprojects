package com.salvation;

public class CLLNode {
	
	private int data;
	
	private CLLNode link;

	public CLLNode(int data) {
		super();
		this.data = data;
	}

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public CLLNode getLink() {
		return link;
	}

	public void setLink(CLLNode link) {
		this.link = link;
	}
	
	

}
